#include "libft.h"

void	bzero(void *ptr, size_t num)
{
	size_t	i;

	i = 0;
	if (num == 0)
		return ;
	while (i < num)
	{
		((unsigned char *)ptr)[i] = 0;
		i++;
	}
}
